export function resolveP10Locale(lang: string | undefined): P10Locale {
  return lang?.startsWith('en') ? 'en-US' : 'zh-CN';
}

export function translateP10(
  key: string,
  locale: P10Locale,
  vars?: Record<string, string | number>,
): string {
  let raw = p10Resources[locale][key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      raw = raw.replaceAll(`{{${name}}}`, String(value));
    }
  }
  return raw;
}
